function Mostrar()
{
	var contador;
	contador=10; 

	while(contador>0) 
	{
		
		 
		document.write("<br>" + contador); // el <br> es un formato de html para que ela cuenta la haga uno debajo del otro (salto de línea)
		contador--;// es lo mismo que poner contador=contador - 1
	}


}//FIN DE LA FUNCIÓN